    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        Valet v = modeloValet.getValet(tabela.getSelectedRow());
        v.setSaida(new Date());
        txtModelo.setText(v.getModelo());
        txtPlaca.setText(v.getPlaca());
        lblEntrada.setText(df.format(v.getEntrada()));
        v.setPreco(this.calcularPreco(v.getEntrada(), v.getSaida()));
        lblPreco.setText("R$ " + String.valueOf(v.getPreco()));
        selecionado = v;
    }//GEN-LAST:event_tabelaMouseClicked

    private double calcularPreco(Date entrada, Date saida) {
        long diferenca = saida.getTime() - entrada.getTime();
        int horas = (int) (diferenca / 1000 / 60 / 60);
        int minutos = (int) ((diferenca / 1000 / 60) - (horas * 60));
        double preco = horas * 4;
        if (minutos != 0) {
            preco += 4;
        }
        return preco;
    }

    private void limparCampos() {
        txtModelo.setText("");
        txtPlaca.setText("");
        lblEntrada.setText("");
        lblPreco.setText("");
    }

